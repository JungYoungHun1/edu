package ch03;

public class OperatorExample4 {
    public static void main(String [] args) {
        int n1 = 10;
        n1++;
        n1++;
        System.out.println(n1);
        n1--;

        System.out.println(n1);

        int n2 = 40;
        System.out.println(++n2);
        System.out.println(n2);

        int n3=50;
        n3+=2;
        n3+=2;
        System.out.println(n3);


    }
}
